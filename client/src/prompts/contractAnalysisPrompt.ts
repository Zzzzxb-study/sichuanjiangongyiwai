/**
 * 构建完整的Prompt（替换模板变量）
 */
export function buildPrompt(template: string, variables: Record<string, string>): string {
  let prompt = template;
  Object.entries(variables).forEach(([key, value]) => {
    prompt = prompt.replace(new RegExp(`{{${key}}}`, 'g'), value);
  });
  return prompt;
}
